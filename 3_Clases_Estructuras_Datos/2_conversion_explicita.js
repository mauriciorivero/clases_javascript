// Cadena → número
let n = Number('100'); // 100
let m = parseInt('3.7'); // 3
console.log(n);
console.log(m);

// Cualquier valor → booleano
let b = Boolean(1); // true
let c = Boolean(''); // false
console.log(b);
console.log(c);
// Número → cadena
let s = String(42); // '42'
let p = 5;

console.log(s+p);